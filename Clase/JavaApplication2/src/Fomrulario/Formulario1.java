    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //capturando el valor de la caja        
        String  codigo=jTextField1.getText();
        //creando una instancia al beanconsulta
        BEAN.BeanConsulta ins=new BEAN.BeanConsulta();
        ResultSet rs1=null;
        try{
        rs1=ins.consulta1(codigo);
        fila=0;
        while(rs1.next()){
        jTable1.setValueAt(rs1.getString(1),fila,0);
        jTable1.setValueAt(rs1.getString(2),fila,1);
        jTable1.setValueAt(rs1.getString(3),fila,2);
        jTable1.setValueAt(rs1.getString(4),fila,3);
        fila++;
        }
        //mostrando la cantidad de registros
        jTextField2.setText(String.valueOf(fila));
        
        }catch(Exception error2){
            JOptionPane.showMessageDialog(null,error2);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

